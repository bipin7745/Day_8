let octalNum = 0o755; 


let hexNum = 0xFF; 


let bigIntNum = 1234567890123456789012345678901234567890n;

let inf = Infinity;
let negInf = -Infinity;
let notANumber = NaN;

console.log("Integer:", intNum);
console.log("Float:", floatNum);
console.log("Exponential:", expNum);
console.log("Binary:", binaryNum);
console.log("Octal:", octalNum);
console.log("Hexadecimal:", hexNum);
console.log("BigInt:", bigIntNum);
console.log("Infinity:", inf);
